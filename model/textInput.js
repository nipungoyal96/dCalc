class TextInput{
    constructor(order,type,field,text,variable,dataType,defaultVal,required){
        this.order=order;
        this.type=type;
        this.field=field;
        this.text=text;
        this.variable=variable;
        this.dataType=dataType;
        this.defaultVal=defaultVal;
        this.required=required;
    }
}